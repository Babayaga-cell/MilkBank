        <h2>What is BreastMilk?</h2>
					<p>Breast milk is a liquid source of food made by a mother's breasts for her children.</p>
					<p>A woman's body creates it in response to pregnancy and the suckling of a baby at the breast—but women who have not been pregnant can also breastfeed, with the help of hormones, medications, and stimulation such as pumping. </p>
					<p>Breast milk provides a child with complete nutrition, as well as protection against infections, diseases, and illnesses. Breastfeeding benefits mothers and children in a variety of ways, and many of the health benefits continue long after breastfeeding has ended.</p>
            <br>
				<h2>The Composition of Breast Milk</h2>
					<p>The composition of breast milk is complex. It consists of over 200 different substances, including protein, fat, carbohydrates, vitamins, minerals, other nutrients, enzymes, and hormones. This composition isn't constant. It's different from mother to mother. It even varies within the same mother.</p>
		    <br>		
			    <h2>How Long It Takes for Breast Milk to Come in?</h2>
					<p>The production of breast milk begins during pregnancy. Then, when your baby is born, you will have a small amount of milk for the first day or two. Don't worry; this little bit is more than enough for your newborn. Your baby is getting enough milk if they have one wet diaper on day one, two wet diapers on day two, and so on.</p>
					<p>By the third day after delivery, the production of breast milk increases. As your breast milk "comes in" you should feel your breasts begin filling up. However, it could take longer (up to five days) for first-time moms to fill up with breast milk.</p>
			<br>		
				<h2>The Stages of Breast Milk</h2>
					<p>Breast milk is typically discussed in three stages: colostrum, transitional breast milk, and mature breast milk.</p>
					<p>Colostrum: Colostrum is the first breast milk. It's present at the end of pregnancy and during the first few days after the birth of your baby. It's usually thick, yellow and sticky, but it can also be thin and white or orange in color. If you are pumping, beware of the colostrum getting stuck in the tubing of your pump due to its thickness. It may be easier to hand-express into a small cup to make sure your baby gets this antibody-rich milk. (You should still use your pump to stimulate your breasts and encourage milk production.)</p>
				  <p>Colostrum is easy for newborns to digest. It's high in protein, low in fat, and contains a high concentration of antibodies, specifically Immunoglobulin A (IgA), as well as white blood cells, to fight off infections. It's also a natural laxative that helps prevent jaundice by clearing your baby's body of meconium: the first thick, black, tarry poop. The amount of colostrum that your body makes is small, but that small volume contains everything your new baby needs in the first few days of life. When a parent induces lactation, colostrum is not produced. In some cases, the birth parent can pump the colostrum and the baby can receive it in a bottle.</p>
					<p>Transitional Breast Milk: Transitional breast milk is a combination of colostrum and mature milk. When your breast milk begins to “come in” at approximately three to five days after delivery, it mixes with the colostrum and gradually transitions to mature milk over the course of a few days or a week.</p>
					<p>Mature Breast Milk: Your milk will change over to mature breast milk by the time your baby is about two weeks old. Mature breast milk is a combination of foremilk and hindmilk. When your child latches on to nurse, the first milk to flow out of your breast is foremilk. Foremilk is thin, watery, and lower in fat and calories. As you continue to breastfeed, the hindmilk will follow. Hindmilk is thicker, creamier, and higher in fat and calories.</p>
			<br>	
				<h2>Making Enough Breast Milk</h2>
					<p>Your body begins to make breast milk in response to pregnancy and delivery of your child. But, to continue to make breast milk after your baby is born, you have to breastfeed or pump your breast milk. The more you breastfeed or pump, the more you'll be telling your body to make breast milk.</p>
					<p>Almost all mothers have the ability to make a healthy breast milk supply. So, if you're worried or struggling with a low milk supply, get help. Most of the time correcting the breastfeeding latch and breastfeeding more often can help turn it around and get things back on track.</p>									
			<br>		
				<h2>The Colors That Breast Milk May Be</h2>
					<p>The colors of breast milk can change throughout the day, or from one day to the next. It's usually white, yellow, or tinted blue. But, depending on what you eat, it could have a green, orange, brown or pink hue. Occasionally, blood from rusty pipe syndrome or cracked nipples can appear in your breast milk.It may be scary, but it isn't dangerous. As long as your baby is not refusing the breast, it's safe to continue to breastfeed if your milk changes color.</p>
